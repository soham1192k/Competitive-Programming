void solve(){
    int n;cin>>n;
    string s;cin>>s;
    map<pair<int,int>,int>mp;
    int d=0,k=0;
    int dp[n];
    dp[0]=1;
    if(s[0]=='D'){
        mp[{1,0}]=1;
        d++;
    }
    else{
        mp[{0,1}]=1;
        k++;
    }
    for(int i=1;i<n;i++){
        if(s[i]=='D'){
            d++;
        }
        else{
            k++;
        }
        int g=__gcd(d,k);
        pair<int,int>now={d/g,k/g};
        dp[i]=1+mp[now];
        mp[now]++;
    }
    for(auto x:dp) cout<<x<<" ";
    cout<<'\n';
}   
