bool ok(int mid,int x,int y,int a,int b){
    int right = floorl((x-mid*b)*1.0l/(a-b));
    int left = ceill((y-mid*a)*1.0l/(b-a));
    if(max(0ll,left)<=min(right,mid)){
        return true;
    }
    return false;
}
void solve(){
    int x,y,a,b;cin>>x>>y>>a>>b;
    if(a==b){
        cout<<min(x,y)/a<<'\n';return;
    }
    if(a<b) swap(a,b);
    int low=0;
    int high=2e9;
    int ans=0;
    while(low<=high){
        int mid=low+(high-low)/2;
        if(ok(mid,x,y,a,b)){
            ans=mid;
            low=mid+1;
        }
        else high=mid-1;
    }
    cout<<ans<<'\n';
}
